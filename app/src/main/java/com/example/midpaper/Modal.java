package com.example.midpaper;

public class Modal {
    private int image;
    private String name;
    private String name1;

    public Modal(int image, String name, String name1){
        this.image = image;
        this.name = name;
        this.name1 = name1;
    }
    public int getImage() {
        return image;
    }

    public  String getName() {
        return name;
    }

    public String getName1() {
        return name1;
    }
}
